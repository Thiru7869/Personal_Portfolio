function wordCount(source: ArticleSource): number {
  const text = `${source.articleBody} ${source.conclusion ?? ""}`;
  return text.split(/\s+/).filter(Boolean).length;
}

function toc(source: ArticleSource): TocEntry[] {
  return [...source.articleBody.matchAll(/^## (.+)$/gm)].map((m) => ({
    id: headingId(m[1]),
    text: m[1],
  }));
}

function enrich(source: ArticleSource): BlogArticle {
  const words = wordCount(source);
  return {
    ...source,
    ...BLOG_AUTHOR,
    canonicalSlug: source.slug,
    wordCount: words,
    readingTime: Math.max(1, Math.round(words / 220)),
    tableOfContents: toc(source),
    featuredImage: `/blog-covers/${categoryCoverSlug[source.category]}.svg`,
    coverImageAlt: `Cover art for "${source.title}" — ${source.category}`,
    ogTitle: source.seoTitle,
    ogDescription: source.seoDescription,
    twitterTitle: source.title,
    twitterDescription: source.description,
  };
}

/** All articles, newest first — the canonical order. */
export const blogArticles: BlogArticle[] = posts
  .map(enrich)
  .sort((a, b) => b.publishDate.localeCompare(a.publishDate));

export const featuredArticles = blogArticles.filter((a) => a.featured);
export const recentArticles = blogArticles.slice(0, 5);

/** "Trending" = featured first, then most-referenced by other posts. */
export const trendingArticles = [...blogArticles]
  .sort((a, b) => {
    const refs = (slug: string) =>
      blogArticles.filter((p) => p.relatedPosts.includes(slug)).length;
    return (
      Number(b.featured) - Number(a.featured) || refs(b.slug) - refs(a.slug)
    );
  })
  .slice(0, 5);

export const blogCategories = [
  ...new Set(blogArticles.map((a) => a.category)),
];

export function articleBySlug(slug: string): BlogArticle | undefined {
  return blogArticles.find((a) => a.slug === slug);
}

/** Prev/next in the canonical (newest-first) order. */
export function adjacentArticles(slug: string): {
  newer: BlogArticle | null;
  older: BlogArticle | null;
} {
  const idx = blogArticles.findIndex((a) => a.slug === slug);
  if (idx === -1) return { newer: null, older: null };
  return {
    newer: idx > 0 ? blogArticles[idx - 1] : null,
    older: idx < blogArticles.length - 1 ? blogArticles[idx + 1] : null,
  };
}

/** Related posts, resolved and padded with same-category picks. */
export function relatedArticles(article: BlogArticle, count = 3): BlogArticle[] {
  const explicit = article.relatedPosts
    .map((slug) => articleBySlug(slug))
    .filter((a): a is BlogArticle => Boolean(a));
  const padding = blogArticles.filter(
    (a) =>
      a.slug !== article.slug &&
      a.category === article.category &&
      !explicit.includes(a)
  );
  return [...explicit, ...padding].slice(0, count);
}

