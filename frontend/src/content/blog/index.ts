/**
 * src/content/blog/index.ts
 * ------------------------------------------------------------
 * Enriches the authored posts with every derivable field —
 * reading time, table of contents, cover art, canonical slug,
 * author block, OG/Twitter fields — so nothing is duplicated
 * and nothing can go stale.
 *
 * To publish a post: append an ArticleSource to posts.ts.
 * Pages, sitemap, search, related links, and the AI assistant
 * pick it up automatically on the next build.
 */

